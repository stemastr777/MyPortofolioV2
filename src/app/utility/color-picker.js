

export function getSectionColor(section) {

    switch (section) {
        case 'Skills':
            return "blue"
        case 'Projects':
            return "red"
        case 'Academic':
            return "orange"
        case 'Organizational Experiences':
            return "green"
        case 'Work Experiences':
            return "purple"
        case 'Certifications':
            return "yellow"
    }

}